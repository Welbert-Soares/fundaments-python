# number integer
number_int = 3
print("Inteiro = ", number_int)

# number float
number_float = 3.14
print("Decimal = ", number_float)

print("tipo da variável inteiro ", type(number_int))
print("tipo da variável decimal ", type(number_float))

sum = number_int + number_float
print("Soma = ", sum)

subtraction = number_int - number_float
print("Subtração = ", subtraction)

multiplication = number_int * number_float
print("Multiplicação = ", multiplication)

division = number_int / number_float
print("Divisão = ", division)

division_int = number_int // number_float
print("Divisão inteira = ", division_int)

exponentiation = number_int**number_float
print("Exponenciação = ", exponentiation)

module = number_int % number_float
print("Módulo = ", module)
